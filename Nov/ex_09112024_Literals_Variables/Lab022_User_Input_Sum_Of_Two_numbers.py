# Write a program to take the 2 user input
# then sum the number
# mul -> *
# div -> /

# Logic Building


# Step 1
# I/P -> num1, num2 -> int
# O/P -> sum - int, sub -> int, div -> float
num1 = int(input("Enter the num 1 "))
num2 = int(input("Enter the num 2 "))

sum = num1+num2
sub = num1-num2
mul = num1*num2
div = num1/num2

print("Sum is : ", sum)
print("Sub is : ", sub)
print("Mul is : ", mul)
print("Div is : ", div)

